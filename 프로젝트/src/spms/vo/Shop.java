package spms.vo;

public class Shop {
	protected int shop_num;
	protected String shop_name;
	protected String shop_address;
	protected String shop_tel;
	protected String shop_type;
	protected String shop_time;
	protected String shop_hp;
	protected String shop_map;
	protected String id;
	protected String text;
	protected String rating;

	public String getShop_map() {
		return shop_map;
	}
	public Shop setShop_map(String shop_map) {
		this.shop_map = shop_map;
		return this;
	}
	
	public int getShop_num() {
		return shop_num;
	}
	public Shop setShop_num(int shop_num) {
		this.shop_num = shop_num;
		return this;
	}
	public String getShop_name() {
		return shop_name;
	}
	public Shop setShop_name(String shop_name) {
		this.shop_name = shop_name;
		return this;
	}
	public String getShop_address() {
		return shop_address;
	}
	public Shop setShop_address(String shop_address) {
		this.shop_address = shop_address;
		return this;
	}
	public String getShop_tel() {
		return shop_tel;
	}
	public Shop setShop_tel(String shop_tel) {
		this.shop_tel = shop_tel;
		return this;
	}
	public String getShop_type() {
		return shop_type;
	}
	public Shop setShop_type(String shop_type) {
		this.shop_type = shop_type;
		return this;
	}
	public String getShop_time() {
		return shop_time;
	}
	public Shop setShop_time(String shop_time) {
		this.shop_time = shop_time;
		return this;
	}
	public String getShop_hp() {
		return shop_hp;
	}
	public Shop setShop_hp(String shop_hp) {
		this.shop_hp = shop_hp;
		return this;
	}
	public String getId() {
		return id;
	}
	public Shop setId(String id) {
		this.id = id;
		return this;
	}
	public String getText() {
		return text;
	}
	public Shop setText(String text) {
		this.text = text;
		return this;
	}
	public String getRating() {
		return rating;
	}
	public Shop setRating(String rating) {
		this.rating = rating;
		return this;
	}
	
	
}










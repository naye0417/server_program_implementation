package spms.vo;

public class LikeList {
	protected String id;
	protected int shop_num;
	protected String shop_name;
	

	public String getId() {
		return id;
	}
	public LikeList setId(String id) {
		this.id = id;
		return this;
	}
	public int getShop_num() {
		return shop_num;
	}
	public LikeList setShop_num(int shop_num) {
		this.shop_num = shop_num;
		return this;
	}
	public String getShop_name() {
		return shop_name;
	}
	public LikeList setShop_name(String shop_name) {
		this.shop_name = shop_name;
		return this;
	}
	
	
}










package spms.vo;

public class Member {
	protected String id;
	protected String pw;
	protected String name;
	protected String birth;
	protected String phone;
	protected String email;
	
	
	public String getId() {
		return id;
	}
	public Member setId(String id) {
		this.id = id;
		return this;
	}
	public String getPw() {
		return pw;
	}
	public Member setPw(String pw) {
		this.pw = pw;
		return this;
	}
	public String getName() {
		return name;
	}
	public Member setName(String name) {
		this.name = name;
		return this;
	}
	public String getBirth() {
		return birth;
	}
	public Member setBirth(String birth) {
		this.birth = birth;
		return this;
	}
	public String getPhone() {
		return phone;
	}
	public Member setPhone(String phone) {
		this.phone = phone;
		return this;
	}
	public String getEmail() {
		return email;
	}
	public Member setEmail(String email) {
		this.email = email;
		return this;
	}
	
	
	
	
	
	
}









